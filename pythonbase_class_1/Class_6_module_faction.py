def square(n):
    """
    square of computation
    :param n: 一个数
    :return: 一个数的平方
    """
    return n ** 2


def cube(n):
    """
    cube of computation
    :param n: 一个数
    :return: 一个数的立方
    """
    return  n ** 3


def num_abs(n):
    """
    abs of computation
    :param n: 一个数
    :return: 一个数的绝对值
    """
    return abs(n)


